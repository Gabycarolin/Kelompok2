package com.example.futsalbismillah.model;

public class FutsalModel {
    String kode, nama_futsal, alamat, telp;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNama_futsal() {
        return nama_futsal;
    }

    public void setNama_futsal(String nama_futsal) {
        this.nama_futsal = nama_futsal;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTelp() {
        return telp;
    }

    public void setTelp(String telp) {
        this.telp = telp;
    }
}
